import { toast } from "react-toastify";

export const successMessage = (message) => {
  // window.alert("Invalid Credentials");
  toast.success(message, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: false,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};

